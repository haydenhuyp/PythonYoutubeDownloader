class DownloadThread(QThread):
    finished = pyqtSignal()

    def __init__(self, url, location, resolution, ui):
        super().__init__()
        self.__url = url
        self.__resolution = resolution
        self.__ui = ui
        self.__location = location

    def run(self):
        """
        Start the thread, either download a low/high resolution video or download audio only. Pass resolution parameter empty string
        to download audio only.
        :return:
        """

        yt = pytube.YouTube(self.__url, on_progress_callback=self.__ui.show_progress, on_complete_callback=self.__ui.complete_func)
        if self.__resolution == 'low_resolution':
            yt.streams.get_lowest_resolution().download(self.__location)
        elif self.__resolution == 'high_resolution':
            yt.streams.get_highest_resolution().download(self.__location)
        else:
            yt.streams.get_audio_only().download(self.__location)
        self.finished.emit()

