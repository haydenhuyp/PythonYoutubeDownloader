    def extract_video_id(self, youtube_link):
        """
        Extract the video id from a valid YouTube link
        :param youtube_link:
        :return: YouTube video ID
        """
        video_id = None
        match = re.search(r"youtube\.com/watch\?v=([A-Za-z0-9\-_]+)", youtube_link)
        if match:
            video_id = match.group(1)
        else:
            match = re.search(r"youtu\.be/([A-Za-z0-9\-_]+)", youtube_link)
            if match:
                video_id = match.group(1)
        return video_id

    def downloadButton_onclick(self):
        self.progressBar.setValue(0)
        self.txtError.setText("")
        link = self.txtLink.toPlainText()
        self.txtStatus.setReadOnly(True)
        youtube_regex = r'^(https?\:\/\/)?(www\.youtube\.com|youtu\.be)\/.+$'

        # Check if the link matches the YouTube regex pattern
        if not re.match(youtube_regex, link):
            self.txtError.setText("Youtube link is not valid")
        else:
            low_resolution = self.chkLowResolution.isChecked()
            audio_only = self.chkAudio.isChecked()
            download_caption = self.chkDownloadCaption.isChecked()
            download_thumbnail = self.chkDownloadThumbnail.isChecked()

            location = str(Path.home() / "Downloads")
            yt = pytube.YouTube(link, on_progress_callback=self.show_progress, on_complete_callback=self.complete_func)
            title = yt.title

            self.stackedWidget.setCurrentWidget(self.page_2)
            self.txtStatus.setText(f"Downloading: {title}")
            # highest or lowest solution
            if low_resolution:
                self.download_thread = DownloadThread(link, location, 'low_resolution', self)
            elif audio_only and not low_resolution:
                self.download_thread = DownloadThread(link, location, '', self)
            else:
                self.download_thread = DownloadThread(link, location, 'high_resolution', self)
            self.download_thread.start()

            # caption
            if download_caption:
                try:
                    transcript = YouTubeTranscriptApi.get_transcript(self.extract_video_id(link), languages=['en'])

                    # in srt format
                    formatter = SRTFormatter()
                    srt_formatted = formatter.format_transcript(transcript)

                    self.txtStatus.setText(self.txtStatus.toPlainText() + '\n' + 'Downloading Caption')

                    with open(location + '\\' + 'subtitle_' + re.sub(r'[^\w\s]', '', title) + '.srt', 'w', encoding='utf-8') as srt_file:
                        srt_file.write(srt_formatted)
                except TranscriptsDisabled:
                    self.txtResult.setText("Caption is not supported for this video")
                except:
                    self.txtResult.setText("Errors in writing Subtitle File")

            # thumbnail
            if download_thumbnail:
                image_url = yt.thumbnail_url
                r = requests.get(image_url, stream=True)
                self.txtStatus.setText(self.txtStatus.toPlainText()+'\n'+'Downloading Thumbnail')
                filename = str(Path.home() / "Downloads") + '/' + title
                if r.status_code == 200:
                    r.raw.decode_content = True

                    with open(filename+'.jpg', 'wb') as f:
                        shutil.copyfileobj(r.raw, f)
                    self.txtResult.setText(self.txtResult.text() + '\nThumbnail Image successfully Downloaded')
                else:
                    self.txtResult.setText(self.txtResult.text() + '\nThumbnail Image Couldn\'t be retrieved')


